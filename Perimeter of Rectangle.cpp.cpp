// Find the Perimeter of Rectangle.
#include <iostream>
using namespace std;
int main()
{
	int  base;
	int height;
	cout<< "Enter the base of Rectangle:-";
	cin >> base;
	cout<< "Enter the height of Rectangle:-";
	cin >> height;
	cout<<"Perimiter of Rectangle is:-"<<2*base*height<<endl;
	return 0;
	
}
