// Find the area of a Traingle whose base and height is given by user.
#include <iostream>
using namespace std;
#define c 0.5
int main()
{
	int base;
	int height;
	// input
	cout <<"\n Enter the base of a Triangle:-";
	cin >> base;
	cout << "\n Enter the height of a Triangle:-";
	cin >> height;
	cout << "Area of a Triangle is: " << c * base * height << endl;
	return 0;
}



